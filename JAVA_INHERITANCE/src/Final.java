/*
 * Final:
 *    final is keyword in java..
 *    It is used to security purpose/protection
 *    There are three uses of final:
 *                          1)Final variable
 *                          2)Final class
 *                          3)Final method
1)Final variable:
  Final variable is like constant in c & c++.
  final variable is not reinitialised,not change,not inc/dec.
  it is fixed value
  syntax:final datatype variablename(values..);

2)Final method:
   final method prevent method overriding
 syntax:
    final returntype mathodname(arg_list);

3)Final class:
   It prevent inheritance i,e final class cannot be extends inherit in.abstract
*/