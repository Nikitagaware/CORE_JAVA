import java.util.*;
public class String_append
{
	public static void main(String[] args)
	{
	   String s1="rbnb";
	   StringBuffer sbr=new StringBuffer(s1);
	   sbr.append("cdj");
	   System.out.println("append String="+sbr);

	}

}
/*===========================output=======
 * append String=rbnbcdj
 * */
